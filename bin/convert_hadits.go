//go:build ignore

// One-shot converter that turns sitrac-v3's PostgreSQL `pg_dump` output into
// SQLite-compatible INSERTs for the HaditsKitab/HaditsBab/Hadits tables.
//
// Usage:
//
//	go run bin/convert_hadits.go <sitrac-dump.sql> <out.sql>
//
// Produces a single SQL file with INSERTs in dependency order (kitab → bab →
// hadits). Idempotent: each INSERT uses `OR IGNORE` so re-running the seed
// against an already-populated DB is a no-op.
package main

import (
	"bufio"
	"fmt"
	"os"
	"regexp"
	"strings"
)

func main() {
	if len(os.Args) < 3 {
		fmt.Fprintln(os.Stderr, "usage: convert_hadits.go <in.sql> <out.sql>")
		os.Exit(2)
	}
	in, err := os.Open(os.Args[1])
	if err != nil {
		fmt.Fprintln(os.Stderr, "open:", err)
		os.Exit(1)
	}
	defer in.Close()

	out, err := os.Create(os.Args[2])
	if err != nil {
		fmt.Fprintln(os.Stderr, "create:", err)
		os.Exit(1)
	}
	defer out.Close()

	w := bufio.NewWriter(out)
	defer w.Flush()

	fmt.Fprintln(w, "-- AUTO-GENERATED by bin/convert_hadits.go. Do not edit by hand.")
	fmt.Fprintln(w, "-- Source: sitrac-v3 backend/prisma/seed-data/base-data.sql.gz")
	fmt.Fprintln(w, "BEGIN TRANSACTION;")

	// Quote-aware statement splitter — pg_dump emits hadith rows whose
	// terjemahan strings contain literal newlines, so a naive line-by-line
	// scan would miss them. We accumulate until we see ';' outside a quoted
	// region.
	br := bufio.NewReaderSize(in, 1<<20)
	var stmt strings.Builder
	inQuote := false
	flush := func() {
		s := strings.TrimLeft(stmt.String(), " \r\n\t")
		stmt.Reset()
		if !strings.HasPrefix(s, "INSERT INTO public.") {
			return
		}
		switch {
		case strings.HasPrefix(s, `INSERT INTO public."HaditsKitab"`):
			fmt.Fprintln(w, convertKitab(s))
		case strings.HasPrefix(s, `INSERT INTO public."HaditsBab"`):
			fmt.Fprintln(w, convertBab(s))
		case strings.HasPrefix(s, `INSERT INTO public."Hadits"`):
			fmt.Fprintln(w, convertHadits(s))
		case strings.HasPrefix(s, `INSERT INTO public."CompactAjar"`):
			fmt.Fprintln(w, convertCompactAjar(s))
		}
	}
	for {
		chunk, err := br.ReadString('\n')
		for i := 0; i < len(chunk); i++ {
			c := chunk[i]
			stmt.WriteByte(c)
			if c == '\'' {
				if inQuote && i+1 < len(chunk) && chunk[i+1] == '\'' {
					stmt.WriteByte(chunk[i+1])
					i++
					continue
				}
				inQuote = !inQuote
			}
			if !inQuote && c == ';' {
				flush()
			}
		}
		if err != nil {
			break
		}
	}
	if stmt.Len() > 0 {
		flush()
	}
	fmt.Fprintln(w, "COMMIT;")
}

// Replacements common to all three tables.
var (
	stripSchema = regexp.MustCompile(`INSERT INTO public\.`)
	camelToSnk  = map[string]string{
		`"namaArab"`:       `nama_arab`,
		`"jumlahHalaman"`:  `jumlah_halaman`,
		`"kitabId"`:        `kitab_id`,
		`"babId"`:          `bab_id`,
		`"teksArab"`:       `teks_arab`,
		`"teksLatin"`:      `teks_latin`,
		`"terjemahanEn"`:   `terjemahan_en`,
		`"sumberAsli"`:     `sumber_asli`,
		`"quranSurah"`:     `quran_surah`,
		`"quranAyat"`:      `quran_ayat`,
		`"haditsId"`:       `hadits_id`,
		`"createdAt"`:      `created_at`,
		`"updatedAt"`:      `updated_at`,
	}
)

func convertKitab(line string) string {
	line = stripSchema.ReplaceAllString(line, `INSERT OR IGNORE INTO `)
	line = strings.Replace(line, `"HaditsKitab"`, `hadits_kitab`, 1)
	for k, v := range camelToSnk {
		line = strings.ReplaceAll(line, k, v)
	}
	return line
}

func convertBab(line string) string {
	line = stripSchema.ReplaceAllString(line, `INSERT OR IGNORE INTO `)
	line = strings.Replace(line, `"HaditsBab"`, `hadits_bab`, 1)
	for k, v := range camelToSnk {
		line = strings.ReplaceAll(line, k, v)
	}
	return line
}

func convertHadits(line string) string {
	line = stripSchema.ReplaceAllString(line, `INSERT OR IGNORE INTO `)
	line = strings.Replace(line, `"Hadits"`, `hadits`, 1)
	for k, v := range camelToSnk {
		line = strings.ReplaceAll(line, k, v)
	}
	return line
}

// CompactAjar rows have a boolean `aktif`. SQLite stores 0/1 integers, so we
// rewrite `, true,` / `, false,` tokens. pg_dump never embeds these tokens
// inside quoted column values for this table, so the rewrite is safe.
func convertCompactAjar(line string) string {
	line = stripSchema.ReplaceAllString(line, `INSERT OR IGNORE INTO `)
	line = strings.Replace(line, `"CompactAjar"`, `compact_ajar`, 1)
	for k, v := range camelToSnk {
		line = strings.ReplaceAll(line, k, v)
	}
	line = strings.ReplaceAll(line, ", true,", ", 1,")
	line = strings.ReplaceAll(line, ", false,", ", 0,")
	return line
}
